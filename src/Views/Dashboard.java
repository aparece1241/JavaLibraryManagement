    /**
     * This function will search a book
     */
    private void searchBook(String bookTitle) {
        ArrayList<Book> books = BookController.getBooks();
        for (Book book : books) {
            if (bookTitle.equals(book.getTitle().toLowerCase())) {
                this.displayBookTitle.setText(book.getTitle());
                this.DisplayBookAuthor1.setText(book.getAuthor());
                this.DisplayBookGenre.setText(book.getGenre());
                this.DisplayBookYear.setText(book.getYear_published().toString());
                this.data.put("searchId", Integer.toString(book.getId()));
                this.showSearchBook();
                return;
            }
        }
        JOptionPane.showMessageDialog(null, "No Book Found!");
    }

    /**
     * Show the borrowed books and the borrower
     */
    private void showBorrowedBooklist() {
        ArrayList<BorrowedBook> borrowedBooks = BookController.getBorrowedBooks();
        DefaultTableModel borrowedTableModel = (DefaultTableModel) BorrowedBookTable.getModel();
        borrowedTableModel.setRowCount(0);
        this.BorrowedBookTable.setModel(borrowedTableModel);
        for (BorrowedBook bookBorrow : borrowedBooks) {
            borrowedTableModel.addRow(new Object[]{bookBorrow.getId(), bookBorrow.getTitle(),
                bookBorrow.getBorrowerName(), bookBorrow.getDueDate(), bookBorrow.getIssueDate()});
        }
        this.BorrowedBookTable.setModel(borrowedTableModel);
    }

    /**
     * This function will show the booklist on the right side
     */
    public void showBookList() {
        DefaultTableModel dataModel = (DefaultTableModel) Table.getModel();
        DefaultTableModel dataModelv2 = (DefaultTableModel) BooksListTable.getModel();
        DefaultTableModel dataModelv3 = (DefaultTableModel) SelectBook.getModel();
        dataModel.setRowCount(0);
        dataModelv2.setRowCount(0);
        dataModelv3.setRowCount(0);
        Table.setModel(dataModel);
        SelectBook.setModel(dataModelv3);
        BooksListTable.setModel(dataModelv2);
        ArrayList<Book> books = BookController.getArrayBook();
        System.out.println(books.size());
        for (Book book : books) {
            dataModel.addRow(new Object[]{book.getTitle(), book.getAuthor()});
            dataModelv2.addRow(new Object[]{book.getId(), book.getTitle(), book.getAuthor(), book.getGenre(),
                book.getYear_published(), book.getStock_number()});
            dataModelv3.addRow(new Object[]{false, book.getId(), book.getTitle(), book.getStock_number()});
        }
        Table.setModel(dataModel);
        SelectBook.setModel(dataModelv3);
        BooksListTable.setModel(dataModelv2);
        DefaultTableCellHeaderRenderer centerRender = new DefaultTableCellHeaderRenderer();
        centerRender.setHorizontalAlignment((int) CENTER_ALIGNMENT);
        BooksListTable.setDefaultRenderer(Integer.class, centerRender);
    }

